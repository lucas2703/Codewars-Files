def decodeMorse(morse_code):
    # ToDo: Accept dots, dashes and spaces, return human-readable message
    morse_code = morse_code.split(' ')
    _result = ""
    for x in morse_code:
        _result += morse_code_dict[x]
    _result = ' '.join(_result.split())
    return _result